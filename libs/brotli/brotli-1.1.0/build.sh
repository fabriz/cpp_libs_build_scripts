buildCurrentArchitecture__ios_clang()
{
    prepareBuildStep "Configuring ${FM_CURRENT_ARCHITECTURE_LIB_TAG} ... "
    "${FM_CONFIG_CMAKE_COMMAND}" ${FM_TARGET_CMAKE_ARGUMENTS_GENERATE} -DBUILD_SHARED_LIBS=False -DBROTLI_BUNDLED_MODE=True > ${FM_CURRENT_ARCHITECTURE_LOG_FILE_CONFIGURE} 2>&1
    checkBuildStep

    prepareBuildStep "Building ${FM_CURRENT_ARCHITECTURE_LIB_TAG} ... "
    "${FM_CONFIG_CMAKE_COMMAND}" ${FM_TARGET_CMAKE_ARGUMENTS_BUILD} > ${FM_CURRENT_ARCHITECTURE_LOG_FILE_MAKE} 2>&1
    checkBuildStep

    prepareBuildStep "Staging ${FM_CURRENT_ARCHITECTURE_LIB_TAG} ... "
    "${FM_CONFIG_CMAKE_COMMAND}" ${FM_TARGET_CMAKE_ARGUMENTS_INSTALL} > ${FM_CURRENT_ARCHITECTURE_LOG_FILE_STAGE} 2>&1
    # Actual staging disabled by BROTLI_BUNDLED_MODE, otherwise the build fails
    createDirectory ${FM_CURRENT_ARCHITECTURE_STAGE_DIR}
    createDirectory ${FM_CURRENT_ARCHITECTURE_STAGE_DIR}/include/brotli
    createDirectory ${FM_CURRENT_ARCHITECTURE_STAGE_DIR}/lib
    copyFile ../c/include/brotli/decode.h ${FM_CURRENT_ARCHITECTURE_STAGE_DIR}/include/brotli/decode.h
    copyFile ../c/include/brotli/encode.h ${FM_CURRENT_ARCHITECTURE_STAGE_DIR}/include/brotli/encode.h
    copyFile ../c/include/brotli/port.h ${FM_CURRENT_ARCHITECTURE_STAGE_DIR}/include/brotli/port.h
    copyFile ../c/include/brotli/shared_dictionary.h ${FM_CURRENT_ARCHITECTURE_STAGE_DIR}/include/brotli/shared_dictionary.h
    copyFile ../c/include/brotli/types.h ${FM_CURRENT_ARCHITECTURE_STAGE_DIR}/include/brotli/types.h
    copyFile libbrotlicommon.a ${FM_CURRENT_ARCHITECTURE_STAGE_DIR}/lib
    copyFile libbrotlicommon.pc ${FM_CURRENT_ARCHITECTURE_STAGE_DIR}/lib
    copyFile libbrotlidec.a ${FM_CURRENT_ARCHITECTURE_STAGE_DIR}/lib
    copyFile libbrotlidec.pc ${FM_CURRENT_ARCHITECTURE_STAGE_DIR}/lib
    copyFile libbrotlienc.a ${FM_CURRENT_ARCHITECTURE_STAGE_DIR}/lib
    copyFile libbrotlienc.pc ${FM_CURRENT_ARCHITECTURE_STAGE_DIR}/lib
    checkBuildStep
}

